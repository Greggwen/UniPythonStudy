#!/bin/python3
"""
递归函数：一个函数在内部调用自已本身。
"""

# 求 n 的乘阶

def fact(n):
    if n == 1: 
        return 1
    return n * fact(n-1)

print(fact(5))

# 计算fact(5)的过程如下：
"""
====> fact(5)
====> 5 * fact(4)
====> 5 * (4 * fact(3))
====> 5 * (4 * (3 * fact(2)))
====> 5 * (4 * (3 * (2 * fact(1))))
====> 5 * (4 * (3 * (2 * 1)))
====> 5 * (4 * (3 * 2))
====> 120
"""

print(fact(1000))

